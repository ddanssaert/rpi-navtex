Fir3::Fir3(Callback on_out) : on_out_(std::move(on_out)) {}

void Fir3::sample_in(double I, double Q) {
    buf_I_[buf_ptr_] = I;
    buf_Q_[buf_ptr_] = Q;

    decim_count_++;
    if ((decim_count_ % DECIMATION_FACTOR) == 0) {
        double sum_I = 0.0, sum_Q = 0.0;
        int conv_ptr = buf_ptr_;

        for (int i = 0; i < FILTER_SIZE; i++) {
            sum_I += filter_h_[i] * buf_I_[conv_ptr];
            sum_Q += filter_h_[i] * buf_Q_[conv_ptr];
            if (conv_ptr == 0)
                conv_ptr = BUF_SIZE - 1;
            else
                conv_ptr--;
        }
        on_out_(sum_I, sum_Q);
        decim_count_ = 0;
    }

    buf_ptr_++;
    buf_ptr_ %= BUF_SIZE;
}
